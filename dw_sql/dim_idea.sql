CREATE TABLE IF NOT EXISTS innk_dw_dev.public.dim_idea (
    id INT IDENTITY(1, 1) PRIMARY KEY NOT NULL UNIQUE,
    idea_db_id INT,
    tag_name VARCHAR(24000),
    tag_description VARCHAR(24000),
    tag_type INT,
    is_private BOOLEAN DEFAULT FALSE,
    category VARCHAR(24000),
    stage VARCHAR(24000),
    like_ideas_count INT,
    average_general FLOAT,
    name VARCHAR(24000),
    description VARCHAR(24000),
    problem_1 VARCHAR(24000),
    solution_1 VARCHAR(24000),
    problem_2 VARCHAR(24000),
    solution_2 VARCHAR(24000),
    problem_3 VARCHAR(24000),
    solution_3 VARCHAR(24000),
    problem_4 VARCHAR(24000),
    solution_4 VARCHAR(24000),
    problem_5 VARCHAR(24000),
    solution_5 VARCHAR(24000),
    problem_6 VARCHAR(24000),
    solution_6 VARCHAR(24000),
    name_embedded VARCHAR(65000),
    prob_1_embedded VARCHAR(65000),
    sol_1_embedded VARCHAR(65000),
    prob_2_embedded VARCHAR(65000),
    sol_2_embedded VARCHAR(65000),
    prob_3_embedded VARCHAR(65000),
    sol_3_embedded VARCHAR(65000),
    prob_4_embedded VARCHAR(65000),
    sol_4_embedded VARCHAR(65000),
    prob_5_embedded VARCHAR(65000),
    sol_5_embedded VARCHAR(65000),
    prob_6_embedded VARCHAR(65000),
    sol_6_embedded VARCHAR(65000),
    combined_embedded VARCHAR(65000),
    created_at TIMESTAMP WITHOUT TIME ZONE   ENCODE az64,
	updated_at TIMESTAMP WITHOUT TIME ZONE   ENCODE az64,
    valid_from TIMESTAMP WITHOUT TIME ZONE   ENCODE az64,
    valid_to TIMESTAMP WITHOUT TIME ZONE   ENCODE az64,
    is_current BOOLEAN DEFAULT TRUE
)